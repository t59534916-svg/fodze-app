// dev-03 LightGBM Runtime — v4 cross-season-validated engine
//
// Browser-side inference for the dev-03 Bayesian Ensemble (5 bagged LightGBM
// Tweedie models per home/away). Plus m6_benter log-pool blender for fusing
// model probs with the (vig-removed) market probs.
//
// Differences from `lgbm-runtime.ts` (v2):
//   - 5-model bagging: predict each model, return mean + variance over the
//     5-prediction distribution (the variance feeds m7_kelly's
//     variance-shrinkage formula).
//   - Categorical features: `league` is the 17th feature; the tree dump uses
//     decision_type "==" with `||`-separated category INDICES. The TS
//     runtime maps a league string to its categorical index via
//     `pandas_categorical[0]` (alphabetical canonical ordering, baked at
//     train-time).
//   - 16 numeric features + 1 categorical. Order is LOCKED — see
//     `FEATURE_NAMES_LOCKED` constant. Feature-builder callers MUST emit
//     values in this exact order.
//
// Source artifact: `public/dev03-model.json` (~7.5 MB), generated by
//   `tools/v4/export_dev03_to_json.py` from the in-repo Python pickles.
// Re-export after each retrain.
//
// Status (2026-05-21): runtime + golden tests shipped. Feature-builder TS
// port (m2_lambda + EloCalculator + TeamMomentumCalculator) is the follow-up
// sprint and not part of this file. Until then, this runtime is a building
// block — Goldilocks UI still displays the "🎯 Dev-03" recommendation badge
// based on `bet-edge-policy.ts`, but auto-routing must wait for features.
// ═══════════════════════════════════════════════════════════════════════

