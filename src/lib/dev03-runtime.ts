/** Lowercased + trimmed league → categorical-index, or -1 if unknown. */
export function leagueToCategoryIndex(league: string): number {
  if (!model) return -1;
  const norm = (league ?? "").toLowerCase().trim();
  const idx = leagueIndex.get(norm);
  return idx ?? -1;
}

// ─── Public API: Predict ────────────────────────────────────────────

/**
 * Convert a Dev03FeatureInput dict to the float-array shape that the trees
 * expect. The `league` string is mapped to its categorical index here.
 *
 * Returns null if any non-categorical feature is non-finite — guarding
 * against silent garbage from the feature builder.
 */
export function buildFeatureArray(input: Dev03FeatureInput): number[] | null {
  const arr: number[] = new Array(FEATURE_NAMES_LOCKED.length);
  for (let i = 0; i < FEATURE_NAMES_LOCKED.length; i++) {
    const name = FEATURE_NAMES_LOCKED[i] as keyof Dev03FeatureInput;
    if (name === "league") {
      arr[i] = leagueToCategoryIndex(input.league);
      // -1 is a valid category sentinel (missing). LightGBM's default-handling
      // routes via `default_left`. No null-return needed here.
      continue;
    }
    const v = input[name] as number;
    if (typeof v !== "number" || !Number.isFinite(v)) {
      console.error(
        `[dev03-runtime] non-finite feature "${name}" = ${v} — refusing to predict`
      );
      return null;
    }
    arr[i] = v;
  }
  return arr;
}

/**
 * Run the full Bayesian-ensemble forward pass. Returns mean + variance for
 * both home + away λ (variance feeds m7_kelly's shrinkage formula).
 *
 * Returns null if model not loaded, or if features are unprocessable.
 *
 * All output lambdas are clamped to [LAMBDA_MIN, LAMBDA_MAX].
 */
export function dev03Predict(input: Dev03FeatureInput): Dev03Prediction | null {
  if (!model) {
    console.error("[dev03-runtime] model not loaded");
    return null;
  }
  const features = buildFeatureArray(input);
  if (!features) return null;

  // Predict on each of the 5 bagged models and aggregate
  const homePreds = model.home_ensemble.models.map(m => predictOneModel(m, features));
  const awayPreds = model.away_ensemble.models.map(m => predictOneModel(m, features));

  const meanH = homePreds.reduce((s, x) => s + x, 0) / homePreds.length;
  const meanA = awayPreds.reduce((s, x) => s + x, 0) / awayPreds.length;
  // ddof=0 to match Python's `var(ddof=0)` — population variance for the
  // ensemble (we have ALL members, not a sample).
  const varH = homePreds.reduce((s, x) => s + (x - meanH) ** 2, 0) / homePreds.length;
  const varA = awayPreds.reduce((s, x) => s + (x - meanA) ** 2, 0) / awayPreds.length;

  // Clamp final lambdas (m2_lambda invariant)
  const lambdaH_mean = Math.max(LAMBDA_MIN, Math.min(LAMBDA_MAX, meanH));
  const lambdaA_mean = Math.max(LAMBDA_MIN, Math.min(LAMBDA_MAX, meanA));

  return {
    lambdaH_mean,
    lambdaH_var: varH,
    lambdaA_mean,
    lambdaA_var: varA,
    lambdaH_per_model: homePreds,
    lambdaA_per_model: awayPreds,
  };
}

// ─── Public API: Benter Blend ───────────────────────────────────────

/**
 * Log-pool softmax blend of model probs with market probs, using per-league
 * Benter weights. Returns blended probs (rows sum to 1).
 *
 * If model not loaded, or benter weights missing for league, falls back to:
 *   1. global_weights (β_model=0.71, β_market=0.30 for dev-03)
 *   2. default_betas (0.5, 0.5)
 *
 * Mirrors the Python `BenterBlender.blend` implementation byte-for-byte
 * (within float64 precision).
 *
 * @param pModel  model probs [H, D, A] — must sum to ~1
 * @param pMarket vig-removed market probs [H, D, A] — must sum to ~1
 * @param league  league code (case-insensitive)
 */
export function dev03BenterBlend(
  pModel: [number, number, number],
  pMarket: [number, number, number],
  league: string,
): [number, number, number] | null {
  if (!model) return null;

  const norm = (league ?? "").toLowerCase().trim();
  const liga = model.benter.liga_weights[norm];

  let beta_model: number;
  let beta_market: number;
  if (liga) {
    beta_model = liga.beta_model;
    beta_market = liga.beta_market;
  } else if (model.benter.global_weights) {
    [beta_model, beta_market] = model.benter.global_weights;
  } else {
    [beta_model, beta_market] = model.benter.default_betas;
  }

  // Clip + log
  const EPS = 1e-12;
  const logPm = pModel.map(p => Math.log(Math.max(EPS, Math.min(1, p))));
  const logPmk = pMarket.map(p => Math.log(Math.max(EPS, Math.min(1, p))));

  // Combined logits
  const logits = logPm.map((lm, i) => beta_model * lm + beta_market * logPmk[i]);

  // Softmax with numerical stability
  const maxLogit = Math.max(...logits);
  const exps = logits.map(x => Math.exp(x - maxLogit));
  const sum = exps.reduce((s, x) => s + x, 0);
  return [exps[0] / sum, exps[1] / sum, exps[2] / sum];
}

/**
 * Returns the per-Liga benter weights (or null if league not fitted).
 * Exposed for telemetry — e.g. "this league uses global-fallback" indicators.
 */
export function getDev03BenterWeights(league: string): BenterLigaWeights | null {
  if (!model) return null;
  const norm = (league ?? "").toLowerCase().trim();
  return model.benter.liga_weights[norm] ?? null;
}

// ─── Public API: Golden Tests ───────────────────────────────────────

/**
 * Run all golden fixtures embedded in the model JSON against the loaded
 * runtime. Returns the count of failures (0 = perfect parity with Python).
 *
 * Tolerance defaults to 1e-4 — matches v2's golden-test convention. The
 * dev-03 ensemble's float64 arithmetic should match Python within ~1e-10
 * for individual tree traversals, but ensemble-average + variance compound
 * up to ~1e-6 in practice.
 */
export function validateDev03GoldenTests(tolerance = 1e-4): {
  passed: number;
  failed: number;
  failures: string[];
} {
  if (!model || !model.golden_tests?.length) {
    return { passed: 0, failed: 0, failures: [] };
  }

  const failures: string[] = [];
  let passed = 0;

  for (const g of model.golden_tests) {
    const pred = dev03Predict(g.features as Dev03FeatureInput);
    if (!pred) {
      failures.push(`${g.name}: null prediction`);
      continue;
    }
    const checks: Array<[string, number, number]> = [
      ["home_mean", pred.lambdaH_mean, g.expected_home_lambda_mean],
      ["home_var", pred.lambdaH_var, g.expected_home_lambda_var],
      ["away_mean", pred.lambdaA_mean, g.expected_away_lambda_mean],
      ["away_var", pred.lambdaA_var, g.expected_away_lambda_var],
    ];
    let allOk = true;
    for (const [tag, got, expected] of checks) {
      const diff = Math.abs(got - expected);
      if (diff > tolerance) {
        failures.push(
          `${g.name}.${tag}: got=${got.toFixed(6)}, expected=${expected.toFixed(6)}, diff=${diff.toExponential(2)}`
        );
        allOk = false;
      }
    }
    if (allOk) passed++;
  }

  return { passed, failed: model.golden_tests.length - passed, failures };
}

