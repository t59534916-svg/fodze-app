/**
 * Locked feature order. The trained model expects exactly these 17 features
 * in this exact order, with `league` (categorical) as the last column.
 *
 * Adding/removing/reordering features requires a new model tag (e.g. dev-04).
 */
export const FEATURE_NAMES_LOCKED: readonly string[] = [
  "home_attack_ratio",
  "home_defense_ratio",
  "away_attack_ratio",
  "away_defense_ratio",
  "home_ess",
  "away_ess",
  "league_home_avg",
  "league_away_avg",
  "league_home_advantage",
  "lambda_h_naive",
  "lambda_a_naive",
  "attack_defense_ratio_h",
  "attack_defense_ratio_a",
  "elo_diff",
  "lineup_quality_diff",
  "form_streak_diff",
  "league",
] as const;

/** Tweedie λ-clamp range (matches m2_lambda's LAMBDA_MIN / LAMBDA_MAX). */
const LAMBDA_MIN = 0.3;
const LAMBDA_MAX = 4.5;

// ─── Module State ───────────────────────────────────────────────────

let model: Dev03Model_FullPayload | null = null;
/**
 * Pre-computed (league → categorical index) lookup. Derived from
 * `pandas_categorical[0]` at load-time. Unknown leagues → -1 (LightGBM's
 * sentinel for missing categorical).
 */
let leagueIndex: Map<string, number> = new Map();

// ─── Tree Traversal ─────────────────────────────────────────────────

/**
 * For categorical-equality splits, the `threshold` is a `||`-separated string
 * of category INDICES that route LEFT. Parse to a Set<number> for O(1) lookup.
 * We cache the parsed set on the node itself to amortize cost across
 * predictions (mutating the loaded JSON in-place is safe — single owner).
 */
interface CategoricalNode extends TreeNode {
  _catSet?: Set<number>;
}

function getCategoricalSet(node: CategoricalNode): Set<number> {
  if (node._catSet) return node._catSet;
  const raw = String(node.threshold ?? "");
  const set = new Set<number>();
  for (const tok of raw.split("||")) {
    const n = Number(tok);
    if (Number.isFinite(n)) set.add(n);
  }
  node._catSet = set;
  return set;
}

function traverseTree(node: TreeNode, features: number[]): number {
  // Leaf
  if (node.leaf_value !== undefined) return node.leaf_value;

  const featIdx = node.split_feature!;
  const val = features[featIdx];
  const dt = node.decision_type;

  // Decide which child to descend
  let goLeft: boolean;
  if (dt === "<=") {
    // Standard numeric inequality. NaN handling matches LightGBM:
    //   - missing val + default_left=true → left
    //   - missing val + default_left=false → right
    if (Number.isNaN(val)) {
      goLeft = !!node.default_left;
    } else {
      goLeft = val <= (node.threshold as number);
    }
  } else if (dt === "==") {
    // Categorical equality. threshold is "1||5||8|…" — go LEFT if val is in the set.
    // Missing categorical (val === -1 or NaN) → default_left
    if (val < 0 || Number.isNaN(val)) {
      goLeft = !!node.default_left;
    } else {
      const set = getCategoricalSet(node as CategoricalNode);
      goLeft = set.has(val);
    }
  } else {
    // Unknown decision_type — default to right (safest, no false confidence)
    console.error(`[dev03-runtime] Unknown decision_type: ${dt}`);
    goLeft = false;
  }

  return traverseTree(goLeft ? node.left_child! : node.right_child!, features);
}

function predictOneModel(m: Dev03Model, features: number[]): number {
  // Sum all leaf values; trees include init_score baked into root tree's leaves.
  // Tweedie uses log-link, so final λ = exp(sum_of_leaves).
  let score = 0;
  for (const t of m.trees) {
    score += traverseTree(t, features);
  }
  return Math.exp(score);
}

// ─── Public API: Load ───────────────────────────────────────────────

/**
 * Load the dev-03 model JSON (typically fetched from `/dev03-model.json`).
 * Returns true on success, false on validation failure.
 *
 * Validates:
 *   - Both ensembles present + non-empty
 *   - n_models matches actual length
 *   - feature_names matches FEATURE_NAMES_LOCKED
 *   - pandas_categorical present + length matches one entry per categorical column
 */
export function loadDev03Model(json: Dev03Model_FullPayload): boolean {
  if (!json.home_ensemble?.models?.length || !json.away_ensemble?.models?.length) {
    console.error("[dev03-runtime] Invalid model: missing ensembles");
    return false;
  }
  if (json.home_ensemble.n_models !== json.home_ensemble.models.length) {
    console.error(
      `[dev03-runtime] home n_models mismatch: declared=${json.home_ensemble.n_models}, actual=${json.home_ensemble.models.length}`
    );
    return false;
  }
  if (json.away_ensemble.n_models !== json.away_ensemble.models.length) {
    console.error(
      `[dev03-runtime] away n_models mismatch: declared=${json.away_ensemble.n_models}, actual=${json.away_ensemble.models.length}`
    );
    return false;
  }
  if (json.feature_names.length !== FEATURE_NAMES_LOCKED.length) {
    console.error(
      `[dev03-runtime] feature dimension mismatch: model=${json.feature_names.length} vs locked=${FEATURE_NAMES_LOCKED.length}`
    );
    return false;
  }
  for (let i = 0; i < json.feature_names.length; i++) {
    if (json.feature_names[i] !== FEATURE_NAMES_LOCKED[i]) {
      console.error(
        `[dev03-runtime] feature[${i}] mismatch: model="${json.feature_names[i]}", locked="${FEATURE_NAMES_LOCKED[i]}"`
      );
      return false;
    }
  }
  if (!json.pandas_categorical?.length || !json.pandas_categorical[0]?.length) {
    console.error("[dev03-runtime] missing pandas_categorical[0] (league index map)");
    return false;
  }

  model = json;

  // Build league → category-index map. LightGBM uses alphabetical order
  // of the pandas Categorical, so the first entry is index 0.
  leagueIndex = new Map();
  json.pandas_categorical[0].forEach((leagueName, idx) => {
    leagueIndex.set(leagueName.toLowerCase().trim(), idx);
  });

  const nHomeTrees = json.home_ensemble.models.reduce((s, m) => s + m.n_trees, 0);
  const nAwayTrees = json.away_ensemble.models.reduce((s, m) => s + m.n_trees, 0);
  console.log(
    `[dev03-runtime] Loaded dev-03 model: ${json.home_ensemble.n_models}×${nHomeTrees / json.home_ensemble.n_models}H + ${json.away_ensemble.n_models}×${nAwayTrees / json.away_ensemble.n_models}A trees, ` +
    `${json.feature_names.length} features, ${leagueIndex.size} leagues, rho=${json.rho}`
  );
  return true;
}

